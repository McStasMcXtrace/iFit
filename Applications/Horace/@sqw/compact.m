% Squeezes the data range in an sqw object to eliminate empty bins
%
% Syntax:
%   >> wout = compact(win)
%
% Input:
% ------
%   win         Input object 
%
% Output:
% -------
%   wout        Output object, with length of axes reduced to yield the
%               smallest cuboid that contains the non-empty bins.
%
%%   Overloaded methods:
%      sqw/compact
%      sqw/compact
%      d4d/compact
%      d3d/compact
%      d2d/compact
%      d1d/compact
%      d0d/compact
%